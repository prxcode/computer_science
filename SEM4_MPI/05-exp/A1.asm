.MODEL SMALL
.STACK 100H

.DATA

STR1 DB 'A','C','E'
STR2 DB 'A','C','E'
LEN  EQU 3

MSG_YES DB 0DH,0AH,'IDENTICAL$'
MSG_NO  DB 0DH,0AH,'NOT IDENTICAL$'

.CODE
START:

MOV AX, @DATA
MOV DS, AX
MOV ES, AX

LEA SI, STR1
LEA DI, STR2
MOV CX, LEN

CLD
REPE CMPSB

JZ MATCHED

LEA DX, MSG_NO
JMP DISPLAY

MATCHED:
LEA DX, MSG_YES

DISPLAY:
MOV AH, 09H
INT 21H

MOV AH, 4CH
INT 21H

END START
