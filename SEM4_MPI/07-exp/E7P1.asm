.MODEL SMALL
.STACK 100H
.DATA
W EQU 30
H EQU 5
.CODE
START:
MOV AH,00
MOV AL,13H
INT 10H
; DRAW UPPER LINE:
MOV CX, 100+W ; COLUMN
MOV DX, 20 ; ROW
MOV AL, 15 ; WHITE
U1: MOV AH, 0CH ; PUT PIXEL
INT 10H
DEC CX
CMP CX, 100
JAE U1
; DRAW BOTTOM LINE:
MOV CX, 100+W ; COLUMN
MOV DX, 20+H ; ROW
MOV AL, 15 ; WHITE
U2: MOV AH, 0CH ; PUT PIXEL
INT 10H
DEC CX
CMP CX, 100
JA U2
; DRAW LEFT LINE:
MOV CX, 100 ; COLUMN
MOV DX, 20+H ; ROW
MOV AL, 15 ; WHITE
U3: MOV AH, 0CH ; PUT PIXEL
INT 10H
DEC DX
CMP DX, 20
JA U3
; DRAW RIGHT LINE:
MOV CX, 100+W ; COLUMN
MOV DX, 20+H ; ROW
MOV AL, 15 ; WHITE
U4: MOV AH, 0CH ; PUT PIXEL
INT 10H
DEC DX
CMP DX, 20
JA U4
MOV AH,00
INT 16H
; RETURN TO TEXT MODE:
MOV AH,00
MOV AL,03 ;TEXT MODE 3
INT 10H
MOV AH,4CH
INT 21H
END START
